PROMPT Creating table: tts_systems...

CREATE TABLE tts_systems (
    system_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    system_name  VARCHAR2(100) UNIQUE NOT NULL,
    description  VARCHAR2(4000),
    is_active    CHAR(1) DEFAULT 'Y' NOT NULL,
    created_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT chk_systems_active CHECK (is_active IN ('Y', 'N'))
);

COMMENT ON TABLE tts_systems IS 'Business systems/applications that tasks are associated with';

-- ------------------------------------------------------------
-- 2.5  tts_tasks — Core tasks table
-- ------------------------------------------------------------
PROMPT Creating table: tts_tasks...

CREATE TABLE tts_tasks (
    task_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_number      VARCHAR2(20)  UNIQUE NOT NULL,
    title            VARCHAR2(250) NOT NULL,
    description      CLOB,
    system_id        NUMBER NOT NULL,
    assigned_to      NUMBER NOT NULL,
    created_by       NUMBER NOT NULL,
    status           VARCHAR2(20) DEFAULT 'CREATED'         NOT NULL,
    priority         VARCHAR2(20) DEFAULT 'MEDIUM'          NOT NULL,
    start_date       DATE NOT NULL,
    due_date         DATE NOT NULL,
    estimated_hours  NUMBER(6,2) NOT NULL,
    actual_hours     NUMBER(6,2) DEFAULT 0,
    completion_date  DATE,
    approval_status  VARCHAR2(20) DEFAULT 'NOT_SUBMITTED'   NOT NULL,
    approved_by      NUMBER,
    approval_notes   VARCHAR2(4000),
    created_at       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_tasks_system   FOREIGN KEY (system_id)   REFERENCES tts_systems(system_id),
    CONSTRAINT fk_tasks_assigned FOREIGN KEY (assigned_to) REFERENCES tts_users(user_id),
    CONSTRAINT fk_tasks_creator  FOREIGN KEY (created_by)  REFERENCES tts_users(user_id),
    CONSTRAINT fk_tasks_approver FOREIGN KEY (approved_by) REFERENCES tts_users(user_id),
    CONSTRAINT chk_tasks_dates     CHECK (due_date >= start_date),
    CONSTRAINT chk_tasks_est_hours CHECK (estimated_hours >= 0),
    CONSTRAINT chk_tasks_act_hours CHECK (actual_hours >= 0)
);

COMMENT ON TABLE  tts_tasks IS 'Core tasks table — tracks all daily tasks with lifecycle management';
COMMENT ON COLUMN tts_tasks.task_number IS 'Human-readable auto-generated ID: TSK-000001';
COMMENT ON COLUMN tts_tasks.approval_status IS 'NOT_SUBMITTED (default), PENDING, APPROVED, REJECTED';
COMMENT ON COLUMN tts_tasks.approval_notes IS 'Manager notes when approving or rejecting a task';

-- ------------------------------------------------------------
-- 2.6  tts_daily_log — Daily time tracking per task
-- ------------------------------------------------------------
PROMPT Creating table: tts_daily_log...

CREATE TABLE tts_daily_log (
    log_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_id      NUMBER NOT NULL,
    user_id      NUMBER NOT NULL,
    log_date     DATE   NOT NULL,
    hours_spent  NUMBER(4,2) NOT NULL,
    notes        VARCHAR2(4000),
    created_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_log_task  FOREIGN KEY (task_id) REFERENCES tts_tasks(task_id) ON DELETE CASCADE,
    CONSTRAINT fk_log_user  FOREIGN KEY (user_id) REFERENCES tts_users(user_id),
    CONSTRAINT chk_log_hours CHECK (hours_spent > 0 AND hours_spent <= 24),
    CONSTRAINT uk_daily_log  UNIQUE (task_id, user_id, log_date)
);

COMMENT ON TABLE  tts_daily_log IS 'Daily time entries — each row represents hours worked on a task for a specific day';
COMMENT ON COLUMN tts_daily_log.hours_spent IS 'Hours worked (0-24), auto-summed into tts_tasks.actual_hours';

