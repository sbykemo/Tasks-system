    CONSTRAINT fk_hist_task FOREIGN KEY (task_id)    REFERENCES tts_tasks(task_id) ON DELETE CASCADE,
    CONSTRAINT fk_hist_user FOREIGN KEY (changed_by) REFERENCES tts_users(user_id)
);

COMMENT ON TABLE  tts_task_history IS 'Complete audit trail of all task changes';
COMMENT ON COLUMN tts_task_history.change_type IS 'Type of change: STATUS, ASSIGNMENT, PRIORITY, APPROVAL, CREATED';

-- ------------------------------------------------------------
-- 2.10 tts_notifications — In-app & email notifications
-- ------------------------------------------------------------
PROMPT Creating table: tts_notifications...

CREATE TABLE tts_notifications (
    notification_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id           NUMBER         NOT NULL,
    task_id           NUMBER,
    notification_type VARCHAR2(50)   NOT NULL,
    message           VARCHAR2(4000) NOT NULL,
    is_read           CHAR(1) DEFAULT 'N' NOT NULL,
    email_sent        CHAR(1) DEFAULT 'N' NOT NULL,
    created_at        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_notif_user  FOREIGN KEY (user_id) REFERENCES tts_users(user_id),
    CONSTRAINT fk_notif_task  FOREIGN KEY (task_id)  REFERENCES tts_tasks(task_id) ON DELETE SET NULL,
    CONSTRAINT chk_notif_read  CHECK (is_read    IN ('Y', 'N')),
    CONSTRAINT chk_notif_email CHECK (email_sent IN ('Y', 'N'))
);

COMMENT ON TABLE  tts_notifications IS 'In-app notifications with optional email delivery tracking';
COMMENT ON COLUMN tts_notifications.notification_type IS 'TASK_ASSIGNED, STATUS_CHANGED, APPROVAL_REQUIRED, APPROVAL_RESULT, COMMENT_ADDED, TASK_OVERDUE';

