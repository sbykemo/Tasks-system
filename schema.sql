COMMENT ON TABLE tts_attachments IS 'File attachments stored as BLOBs, linked to tasks';

-- ------------------------------------------------------------
-- 2.9  tts_task_history — Audit trail for task changes
-- ------------------------------------------------------------
PROMPT Creating table: tts_task_history...

CREATE TABLE tts_task_history (
    history_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_id      NUMBER NOT NULL,
    changed_by   NUMBER NOT NULL,
    changed_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    change_type  VARCHAR2(50) NOT NULL,
    old_value    VARCHAR2(4000),
