PROMPT Creating table: tts_lookups...

CREATE TABLE tts_lookups (
    lookup_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lookup_type     VARCHAR2(50)  NOT NULL,
    lookup_code     VARCHAR2(50)  NOT NULL,
    display_name    VARCHAR2(100) NOT NULL,
    display_name_ar VARCHAR2(100),
    display_order   NUMBER DEFAULT 0,
    is_active       CHAR(1) DEFAULT 'Y' NOT NULL,
    CONSTRAINT chk_lookups_active CHECK (is_active IN ('Y', 'N')),
    CONSTRAINT uk_lookups UNIQUE (lookup_type, lookup_code)
);

COMMENT ON TABLE  tts_lookups IS 'Centralized lookup/reference values for all dropdowns and statuses';
COMMENT ON COLUMN tts_lookups.lookup_type IS 'Category: TASK_STATUS, PRIORITY, APPROVAL_STATUS, NOTIFICATION_TYPE';
COMMENT ON COLUMN tts_lookups.display_name_ar IS 'Arabic display label for bilingual support';

-- ------------------------------------------------------------
-- 2.2  tts_departments — Organizational departments
-- ------------------------------------------------------------
PROMPT Creating table: tts_departments...

CREATE TABLE tts_departments (
    dept_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dept_name    VARCHAR2(100) UNIQUE NOT NULL,
    dept_head_id NUMBER,
    is_active    CHAR(1) DEFAULT 'Y' NOT NULL,
    created_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT chk_dept_active CHECK (is_active IN ('Y', 'N'))
);

COMMENT ON TABLE tts_departments IS 'Organizational departments for grouping users';

-- ------------------------------------------------------------
-- 2.3  tts_users — System users (employees, managers, admins)
-- ------------------------------------------------------------
PROMPT Creating table: tts_users...

CREATE TABLE tts_users (
    user_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username      VARCHAR2(50)  UNIQUE NOT NULL,
    password_hash VARCHAR2(256) NOT NULL,
    password_salt VARCHAR2(128) NOT NULL,
    email         VARCHAR2(100) UNIQUE NOT NULL,
    full_name     VARCHAR2(150) NOT NULL,
    role          VARCHAR2(20)  NOT NULL,
    dept_id       NUMBER,
    manager_id    NUMBER,
    is_active     CHAR(1) DEFAULT 'Y' NOT NULL,
    last_login    TIMESTAMP,
    created_at    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT chk_users_active CHECK (is_active IN ('Y', 'N')),
    CONSTRAINT fk_users_dept    FOREIGN KEY (dept_id)    REFERENCES tts_departments(dept_id),
    CONSTRAINT fk_users_manager FOREIGN KEY (manager_id) REFERENCES tts_users(user_id)
);

COMMENT ON TABLE  tts_users IS 'All system users with authentication and role information';
COMMENT ON COLUMN tts_users.role IS 'User role: ADMIN, MANAGER, EMPLOYEE';
COMMENT ON COLUMN tts_users.password_salt IS 'Random salt used with SHA-256 hashing for password security';

-- Add the deferred FK from departments back to users
ALTER TABLE tts_departments ADD CONSTRAINT fk_dept_head 
    FOREIGN KEY (dept_head_id) REFERENCES tts_users(user_id);

