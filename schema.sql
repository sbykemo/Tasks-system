    CONSTRAINT fk_attach_task FOREIGN KEY (task_id)     REFERENCES tts_tasks(task_id) ON DELETE CASCADE,
    CONSTRAINT fk_attach_user FOREIGN KEY (uploaded_by) REFERENCES tts_users(user_id)
